package cn.itheima.domain;

public class Kaoqinjilu {

	private int id;
	private String kaoqinshijian;
	private String leibie;
	private String staff_name;
	private String kaoqinshiduan;
	private String shuoming;
	private String jiluren;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getKaoqinshijian() {
		return kaoqinshijian;
	}

	public void setKaoqinshijian(String kaoqinshijian) {
		this.kaoqinshijian = kaoqinshijian;
	}

	public String getLeibie() {
		return leibie;
	}

	public void setLeibie(String leibie) {
		this.leibie = leibie;
	}

	public String getStaff_name() {
		return staff_name;
	}

	public void setStaff_name(String staff_name) {
		this.staff_name = staff_name;
	}

	public String getKaoqinshiduan() {
		return kaoqinshiduan;
	}

	public void setKaoqinshiduan(String kaoqinshiduan) {
		this.kaoqinshiduan = kaoqinshiduan;
	}

	public String getShuoming() {
		return shuoming;
	}

	public void setShuoming(String shuoming) {
		this.shuoming = shuoming;
	}

	public String getJiluren() {
		return jiluren;
	}

	public void setJiluren(String jiluren) {
		this.jiluren = jiluren;
	}

}
