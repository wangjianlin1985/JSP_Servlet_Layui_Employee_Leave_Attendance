package cn.itheima.domain;

public class Chuchaishenqing {

	private int id;
	private String chuchaishijian;
	private String chuchaitianshu;
	private String tongxingrenyuan;
	private String mudidi;
	private String chuxingfangshi;
	private String shiyou;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getChuchaishijian() {
		return chuchaishijian;
	}
	public void setChuchaishijian(String chuchaishijian) {
		this.chuchaishijian = chuchaishijian;
	}
	public String getChuchaitianshu() {
		return chuchaitianshu;
	}
	public void setChuchaitianshu(String chuchaitianshu) {
		this.chuchaitianshu = chuchaitianshu;
	}
	public String getTongxingrenyuan() {
		return tongxingrenyuan;
	}
	public void setTongxingrenyuan(String tongxingrenyuan) {
		this.tongxingrenyuan = tongxingrenyuan;
	}
	public String getMudidi() {
		return mudidi;
	}
	public void setMudidi(String mudidi) {
		this.mudidi = mudidi;
	}
	public String getChuxingfangshi() {
		return chuxingfangshi;
	}
	public void setChuxingfangshi(String chuxingfangshi) {
		this.chuxingfangshi = chuxingfangshi;
	}
	public String getShiyou() {
		return shiyou;
	}
	public void setShiyou(String shiyou) {
		this.shiyou = shiyou;
	}
	
	
}
